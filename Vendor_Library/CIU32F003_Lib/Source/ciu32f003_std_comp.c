/** 
* @brief  COMP去初始化
* @param  compx COMP外设
* @retval 无
*/
void std_comp_deinit(COMP_t *compx)
{
    /* 复位COMP外设 */
    compx->CSR = 0x00000000U;
    
    /*当COMP1和COMP2都复位时，复位CR寄存器*/
    if((COMP1->CSR == 0) && (COMP2->CSR == 0))
    {
        COMP_COMMON->CR = 0x00000000U;
    }
    
}

